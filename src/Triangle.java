public class Triangle {
    public static void main(String[] args) {
        for(int i=0;i<=3;i++){
            int stars = 2 * i - 1;
            for (int j = 1; j <= stars; j++) {
                System.out.print("*");
                if (j < stars) {
                    System.out.print(" ");
                }
            }
            System.out.println();
        }
            }
        }
    

